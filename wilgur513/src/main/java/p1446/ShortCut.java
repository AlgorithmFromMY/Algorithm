package p1446;

public class ShortCut {
    public final int id;
    public final int from;
    public final int to;
    public final int distance;

    public ShortCut(int id, int from, int to, int distance) {
        this.id = id;
        this.from = from;
        this.to = to;
        this.distance = distance;
    }
}
